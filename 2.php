                            <h4>Обзор</h4>
                         <p>Для того, чтобы начать игру, вам нужно всего лишь зарегистрироваться на официальном сайте и скачать клиент. Игра бесплатна, ничего платить не надо.

                            Вначале вы получаете в своё распоряжение по одному танку на каждую из наций: Советский Союз, США, Германия, Япония, Франция, Великобритания и даже Китай.
                            
                            После входа в игру вы попадаете в гараж, в котором у вас будет куча свободных слотов. В каждом их слотов можно поместить только один танк. Дополнительные слоты можно покупать за золото. Однако создать гараж из 100 танков у вас не получится, да в этом и нет смысла.
                            
                            Чтобы начать играть за любую из наций, вам достаточно зайти в магазин и приобрести за ноль монет танк первого уровня.
                            
                            Ваш первый танк, естественно, самый убогий в игре — провезёт вас через первое сражение, которое вы, скорее всего, с треском провалите, что и понятно..Всему своё время.
                            
                            Теперь о визуальной составляющей. Графика и дизайн игры довольно качественные, хотя и значительно уступают своему главному конкуренту — игре War Thunder. Но в целом детализация танков приличная. А вот физика не на высоте — какое-то здание, к примеру, можно разрушить, а какое-то нет, но это всё мелочи, практически не влияющие на уровень удовольствия, получаемого от геймплея.
                            
                            Физика повреждений у танков присутствует, но тоже не вполне реалистичная - отображается в виде царапин. То есть здесь нет такого, например, чтобы у вас башня отваливалась, хотя спалить и разрушить танк вполне можно.
                            
                            За очки опыта вы можете модернизировать и открывать новые танки, Модернизация включает в себя смену двигателя, башни, пушки, шасси, рации и многого другого.
                            
                            Особое значение во всём этом имеет пушка. Так же в игре большое значение имеют снаряды - их покупают за заработанные в боях деньги.
                            
                            От мощи и типа снаряда зависит его цена. Некоторые снаряды стоят 1000 кредитов. Для сравнения — за бой вы можете получить, в среднем, 10 тысяч кредитов, из них за починку танка вы отдадите 5 тысяч. Соответственно, остальные 5 тысяч — это всего лишь 5 выстрелов. А ведь в случае неудачного боя, этих кредитов вы можете и не получить! Поэтому игра на некоторых танках может оказаться не только не прибыльной, но даже убыточной.
                            
                            У начальных же танков есть одна фишка — они вообще не требуют ремонта, и снаряды для них бесплатны! Соответственно, всё, что вы на них получите, пойдёт только в плюс, хотя на начальных танках, как вы понимаете, не заработаешь много.
                            
                            В чём основная мотивация игры? Для большинства — это прокачка одного основного танка до максимума, чтобы нагнуть всех! Сконцентрироваться на прокачке одного танка намного эффективнее, чем пытаться прокачивать одновременно несколько. Однако нужно учитывать, что у врага может оказаться ещё более топовый танк, чем у вас, не считая тех случаев, когда на вас могут тупо навалиться толпой. Так что получить танк, который гарантировано нагнёт всех, практически невозможно. Эта хитрость разработчиков, побуждает игроков хотеть всё более и более мощный танк, а значит, в игру вы втянетесь надолго.

                            WoT – это тактический шутер, соответственно, особое значение здесь имеет тактика. Каждый танк предназначен для своей особой цели. Лучше каждый из них использовать по назначению, в противном случае, ничего хорошего не получится.
                            
                            Про тактику каждого их типов танков можно говорить часами. К примеру, лёгкие танки заточены на то, чтобы «светить», то есть служить в качестве разведчиков. Их задача — ехать на большой скорости в сторону вражеской базы, выявляя местоположение вражеских танков. Эта информация очень важна для работы союзной артиллерии и других танков.
                            
                            Для танков, не обладающих внушительной бронёй, лучшей тактикой будет — высиживать в засаде. Ну а ломиться в гущу боя на артиллерии — просто смешно. Задача арты — сидеть на базе, находясь в глубоком тылу, или на худой конец — прятаться в кустах.</p>
