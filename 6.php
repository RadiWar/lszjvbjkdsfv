                         <p>Суть Майнкрафта заключается в его безграничных возможностях. Это огромная песочница, в которой можно делать абсолютно всё: выживать, строить огромные замки и сложные механизмы и даже программировать. Игра именно такой и задумывалась. В ней нет страшных сцен, бездумной стрельбы и прочего негатива, присущего современным играм. Майнкрафт сегодня известен во всем мире. Игра обзавелась огромным количеством сопутствующих продуктов, которые принесли создателям баснословную прибыль и миллионные тиражи. Далеко не каждая игровая продукция в современном мире достигает таких впечатляющих успехов.

                            Но любая компьютерная игра начинается с небольшого проекта, и Майнкрафт — не исключение. Рарзработчик Маркус Перссон (более известный как Notch) заинтересовался программированием и созданием игр еще в детстве. Первая собственная игра была написана им в возрасте 9 лет. Хотя свой главный проект Перссон создаст гораздо позже. До этого момента автор интересовался различными «симуляторами бога» и классическими играми жанра RPG. Они-то и подтолкнули будущего миллиардера к созданию Майнкрафт — настоящего виртуального приключения, вобравшего в себя элементы всех любымых Перссоном игр. Сперва проект носил название Cave Game, затем он был переименован в Minecraft. Но появившись в 2009 году, игра была еще достаточно сырой, с большим количеством ошибок в коде. Это не помешало собрать вокруг проекта целое сообщество поклонников. В чем суть Майнкрафта, так быстро завоевавшего такую популярность уже с первых версий? В первую очередь игра представляет собой безграничную «песочницу», позволяющую создавать практически любой объект. Аналогов этой концепции на тот момент еще не существовало.

                            Процесс игры заключается в выживании в созданных игроками условиях. Необходимо постоянно защищаться или прятаться от внешних угроз, проникающих в виртуальный мир. Проект примечателен еще и самостоятельной раскруткой самими разработчиками, без привлечения издательств. А популярность Майнкрафту обеспечило «сарафанное радио». Игра упоминалась в интернете буквально повсюду, но при этом официальная реклама отсутствовала. Это не помешало игре стать по истине культовой среди геймеров. Сам Маркус Перссон однажды решил отойти от разработки игры в рамках компании King, где он числился программистом, и сосредоточиться на работе в собственной студии Mojang AB. Майнкрафт постоянно развивался и развивается до сих пор. Текущая команда разработчиков выпускает новые версии игры, исправляет ошибки, добавляет новые элементы. Модификации также создаются и самими поклонниками.</p>
