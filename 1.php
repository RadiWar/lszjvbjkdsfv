                            <h4>Обзор</h4>
                         <p>Такое впечатление, будто Counter-Strike: Global Offensive прибыла из другого измерения.

                            Эта игра не то что не должна была существовать — она просто не нужна. ПК-юзеры (если верить SteamGraph, то тысячи их) вполне удовлетворялись Counter-Strike: Source и CS 1.6, которые полировались чуть ли не десятилетиями и получили море внимания от игроков.
                            
                            Однако CS:GO существует, и она тупо заполнена клонами пистолета Desert Eagle и карты de_dust, что наводит на мысль о разрыве в пространственно-временном континууме. Вдруг гейм-индустрия параллельной вселенной смешалась с нашей?
                            
                            Играть в CS:GO — всё равно что сходить на встречу выпускников. Сразу заметны изменения: постарели, постриглись, переоделись, в конце концов. Но это даже доставляет удовольствие: это ведь всё те же люди, которых вы знали!
                            
                            Другими словами, знакомость CS:GO — одновременно и хорошо, и плохо. Даже минимальные отклонения от классики CS сразу бросаются в глаза. MP-5 теперь называется MP-7, но это всё ещё та же «не убивай меня, плиз, это всё, что я смог купить». TMP отныне заменила MP9. Рэгдолл-физика после смерти больше не заставляет глаза кровоточить. А ещё к М4 почему-то нельзя прикрепить глушитель. Однако оружие — это полбеды. Конечно, можно жить полноценной жизнью и без точной реставрации МР5 из начала двухтысячных, но намного больше парит не бездумное копирование, а система взаимодействия между игроками. Изменения в ней малозаметны, но они есть. Если выстрелить в кого-то в GO, он даже не вздрогнет. Да, кровь хлещет, звук воспроизводится соответствующий (если расстояние позволяет), но он продолжит стоять неподвижно. Что заставило разработчиков убрать эту анимацию — непонятно. Из-за этого очень часто непонятно, наносится врагу урон или нет — особенно на дальних расстояниях. А потому и так называемые трейсеры становятся незаменимыми: они позволяют увидеть траекторию пули в течение ещё нескольких миллисекунд. Стреляя из винтовки или SMG, можно легко отвлечься от врага и прицела, за которыми следить необходимо, чтобы попытаться понять, куда попала пуля, по мельчайшим и подтормаживающим эффектам. Небольшое преимущество трассеров именно в том, что они облегчают эту задачу.</p>
